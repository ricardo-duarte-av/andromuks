    fun getSuggestions(
        query: String,
        customEmojiPacks: List<AppViewModel.EmojiPack>,
        maxResults: Int = 25
    ): List<EmojiSuggestion> {
        val trimmed = query.trim().lowercase()

        val standardMatches = shortcodeToEmoji
            .asSequence()
            .filter { (name, _) ->
                trimmed.isEmpty() || name.startsWith(trimmed)
            }
            .sortedBy { it.key }
            .map { (name, emoji) -> EmojiSuggestion(shortcode = name, emoji = emoji) }

        val customMatches = customEmojiPacks
            .asSequence()
            .flatMap { it.emojis.asSequence() }
            .filter { emoji ->
                val name = emoji.name.lowercase()
                trimmed.isEmpty() || name.startsWith(trimmed)
            }
            .sortedBy { it.name }
            .map { emoji -> EmojiSuggestion(shortcode = emoji.name, customEmoji = emoji) }

        return (standardMatches + customMatches).take(maxResults).toList()
    }

    fun findByShortcode(
        shortcodeWithoutColons: String,
        customEmojiPacks: List<AppViewModel.EmojiPack>
    ): EmojiSuggestion? {
        val key = shortcodeWithoutColons.trim().lowercase()
        shortcodeToEmoji[key]?.let { return EmojiSuggestion(shortcode = key, emoji = it) }
        customEmojiPacks
            .asSequence()
            .flatMap { it.emojis.asSequence() }
            .firstOrNull { it.name.equals(key, ignoreCase = true) }
            ?.let { return EmojiSuggestion(shortcode = it.name, customEmoji = it) }
        return null
    }
}

@Composable
fun EmojiSuggestionList(
    query: String,
    customEmojiPacks: List<AppViewModel.EmojiPack>,
    homeserverUrl: String,
    authToken: String,
    onSuggestionSelected: (EmojiSuggestion) -> Unit,
    modifier: Modifier = Modifier
) {
    val suggestions = remember(query, customEmojiPacks) {
        EmojiShortcodes.getSuggestions(query, customEmojiPacks)
    }

    if (suggestions.isEmpty()) return

    Surface(
        modifier = modifier
            .widthIn(max = 260.dp)
            .border(
                width = 1.dp,
                color = MaterialTheme.colorScheme.outlineVariant,
                shape = RoundedCornerShape(16.dp)
            ),
        shape = RoundedCornerShape(16.dp),
        color = MaterialTheme.colorScheme.surface,
        tonalElevation = 8.dp
    ) {
        LazyColumn(
            modifier = Modifier.height(200.dp),
            contentPadding = PaddingValues(8.dp),
            verticalArrangement = Arrangement.spacedBy(2.dp)
        ) {
            items(suggestions) { suggestion ->
                Row(
                    modifier = Modifier
                        .clickable { onSuggestionSelected(suggestion) }
                        .padding(horizontal = 12.dp, vertical = 6.dp),
                    verticalAlignment = Alignment.CenterVertically
                ) {
                    Box(
                        modifier = Modifier
                            .width(32.dp)
                            .height(32.dp),
                        contentAlignment = Alignment.Center
                    ) {
                        when {
                            suggestion.emoji != null -> Text(
                                text = suggestion.emoji,
                                style = MaterialTheme.typography.titleLarge
                            )
                            suggestion.customEmoji != null -> ImageEmoji(
                                mxcUrl = suggestion.customEmoji.mxcUrl,
                                homeserverUrl = homeserverUrl,
                                authToken = authToken
                            )
                        }
                    }

                    Spacer(modifier = Modifier.width(12.dp))

                    Text(
                        text = ":${suggestion.shortcode}:",
                        style = MaterialTheme.typography.bodyMedium.copy(fontWeight = FontWeight.Medium),
                        color = MaterialTheme.colorScheme.onSurface
                    )
                }
            }
        }
    }
