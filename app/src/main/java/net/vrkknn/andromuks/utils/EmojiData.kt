package net.vrkknn.andromuks.utils

object EmojiData {

    fun getEmojiCategories(): List<EmojiCategory> = emojiCategories

    fun getAllEmojis(): List<String> = emojiCategories.flatMap { it.emojis }

    fun searchEmojis(query: String): List<String> {
        val q = query.trim().lowercase()
        if (q.isBlank()) return emptyList()
        return getAllEmojis().filter { emoji ->
            emojiNameMap[emoji]?.contains(q) == true ||
            emojiShortcodesMap[emoji]?.contains(q) == true
        }
    }

    fun getEmojiName(emoji: String): String? = emojiNameMap[emoji]
}

