/**
 * Comprehensive Unicode Standard Emoji dataset organized by official categories.
 * Based on Unicode Standard Emoji 15.0+ specification with over 3,900 emojis.
 */
object EmojiData {
    
    /**
     * Get all emoji categories in the official Unicode Standard Emoji order
     */
